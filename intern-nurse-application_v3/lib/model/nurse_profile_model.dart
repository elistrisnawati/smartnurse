class NurseProfileModel {
  final String name;
  final String birthDate;
  final String phoneNumber;
  final String campus;
  final String educationMajor;
  final String email;
  final String internHospital;
  final String clinicInstructor;

  NurseProfileModel({
    required this.name,
    required this.birthDate,
    required this.phoneNumber,
    required this.campus,
    required this.educationMajor,
    required this.email,
    required this.internHospital,
    required this.clinicInstructor,
  });
}
